def sales():
    d = {}
    try:
        while True:
            inp = input()
            name, prod, c = inp.split()
            if d.get(name, None) is None:
                d[name] = {prod: int(c)}
            else:
                if d[name].get(prod, None) is None:
                    d[name].update({prod: int(c)})
                else:
                    d[name].update({prod: d[name][prod] + int(c)})
    except EOFError:
        pass
    for key, value in sorted(d.items()):
        print(key + ':')
        for k, v in sorted(value.items()):
            print(k, v)


