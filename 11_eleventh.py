def analysis():
    d = {}
    for _ in range(int(input())):
        x = input().split()
        for s in x:
            d[s] = d.get(s, 0) - 1
    d_rev = tuple(zip(d.values(), d.keys()))
    for key, value in sorted(d_rev):
        print(value)


